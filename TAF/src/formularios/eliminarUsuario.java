    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        tabla.setRowCount(0);   
    tabla.setColumnCount(0);
    
    tabla.addColumn("Nombre Usuario");
    
    
        try{
            tablaUsuarios.setModel(tabla);
             rs= clases.usuario.verUsuario(cx, txtUsuario.getText());
             // while(rs.next()){
             if(rs.next()){
                   datos[0]=rs.getString("nombre_usuario");
                   
                   datos[1]=rs.getString("codigo");
                   
                   tabla.addRow(datos);
                   codigoU.setText(datos[1].toString());
                   activarEliminar();
                   
             }else{
                  JOptionPane.showMessageDialog(null, "No existe el usuario "+txtUsuario.getText(),"ERROR",ERROR_MESSAGE); 
             }     
              //}
             
              
          }catch(Exception e){
                   JOptionPane.showMessageDialog(null, "Ha ocurrido un error al buscar al usuario","ERROR",ERROR_MESSAGE); 
          }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
         if (txtUsuario.getText().isEmpty() ){
             JOptionPane.showMessageDialog(null, "Campo vacío","ERROR",ERROR_MESSAGE);
             return;
        }
         
          int respuesta = JOptionPane.showConfirmDialog(
       null,
       "¿Estás seguro de que deseas eliminar a este usuario?",
       "Confirmar eliminación",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE
       );
        
        if (respuesta == JOptionPane.YES_OPTION){
           int codigo= Integer.parseInt(codigoU.getText());
           try{
              clases.usuario.eliminar(cx, codigo);
              JOptionPane.showMessageDialog(null, "Usuario eliminado correctamente");
              desactivarEliminar();
           }catch(Exception e){
              JOptionPane.showMessageDialog(null, "Ha ocurrido un error al eliminar el usuario","ERROR",ERROR_MESSAGE); 
           }
        }else{
            JOptionPane.showMessageDialog(null, "Se ha cancelado la eliminación");
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

