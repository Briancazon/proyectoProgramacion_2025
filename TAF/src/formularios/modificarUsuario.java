    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        int codigo= Integer.parseInt(codigoU.getText());
        try{
         clases.usuario.update(cx,txtUsuario2.getText(), txtContraseña.getText(), codigo);
         desactivarGuardar();
         desactivarCancelar();
         cancelar();
         limpìar();
     }catch(Exception e){
         JOptionPane.showMessageDialog(null, "Ha ocurrido un error al actualizar el usuario","ERROR",ERROR_MESSAGE); 
     }        
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
     tabla.setRowCount(0);   
    tabla.setColumnCount(0);
    //tabla.addColumn("Código");
    tabla.addColumn("Nombre Usuario");
    tabla.addColumn("Contraseña");
    
        try{
            tablaUsuarios.setModel(tabla);
             rs= clases.usuario.verUsuario(cx, txtUsuario.getText());
            //  while(rs.next()){
            if(rs.next()){
                
                   datos[0]=rs.getString("nombre_usuario");
                   datos[1]=rs.getString("contraseña_usuario");
                   datos[2]=rs.getString("codigo");
                   
                   tabla.addRow(datos);
                   codigoU.setText(datos[2].toString());
            }else{
                 JOptionPane.showMessageDialog(null, "No existe el usuario "+txtUsuario.getText(),"ERROR",ERROR_MESSAGE); 
            }      
             // }
             
              
          }catch(Exception e){
                   JOptionPane.showMessageDialog(null, "Ha ocurrido un error al mostrar  la  xona","ERROR",ERROR_MESSAGE); 
          }
            
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        try{
            int filaSeleccionada = tablaUsuarios.getSelectedRow();
            
            String usuario =tablaUsuarios.getValueAt(filaSeleccionada, 0).toString();
            String contra =tablaUsuarios.getValueAt(filaSeleccionada, 1).toString();
      
            txtUsuario2.setText(usuario);
            txtContraseña.setText(contra);
           
           
       }catch(Exception e){
            JOptionPane.showMessageDialog(null, "No hay datos en la tabla","ERROR",ERROR_MESSAGE);
       }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

